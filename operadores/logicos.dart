/**
 * operadores lógicos 
 * usados para combinar expressões booleanas
 * && = AND (E) 
 * || = OR (OU)
 * ! negacao
 * suas combinações mostram os resultados seguindo as regras abaixo
 *  true && true = true
 *  true && false = false
 *  false && false = false
 *  true || true = true
 *  true || false = true
 *  false || false = false
 *  !true = false
 *  !false = true
 */